package pvt.disney.dti.gateway.data.common;
/**
 * The Class CommandTO.
 * @author MISHP012
 *
 */
public class CommandTO {
   
   /** The cmd id. */
   private String cmdId; 
   
   /** The cmd code. */
   private String cmdCode;
   
   /**
    * Gets the cmd id.
    *
    * @return the cmd id
    */
   public String getCmdId() {
      return cmdId;
   }
   
   /**
    * Sets the cmd id.
    *
    * @param cmdId the new cmd id
    */
   public void setCmdId(String cmdId) {
      this.cmdId = cmdId;
   }
   
   /**
    * Gets the cmd code.
    *
    * @return the cmd code
    */
   public String getCmdCode() {
      return cmdCode;
   }
   
   /**
    * Sets the cmd code.
    *
    * @param cmdCode the new cmd code
    */
   public void setCmdCode(String cmdCode) {
      this.cmdCode = cmdCode;
   } 
   
}
